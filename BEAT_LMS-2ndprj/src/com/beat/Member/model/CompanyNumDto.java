package com.beat.Member.model;

public class CompanyNumDto {

	private int comnum;
	private String name;
	private int combirth;
	private int comzen;
	public int getComnum() {
		return comnum;
	}
	public void setComnum(int comnum) {
		this.comnum = comnum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCombirth() {
		return combirth;
	}
	public void setCombirth(int combirth) {
		this.combirth = combirth;
	}
	public int getComzen() {
		return comzen;
	}
	public void setComzen(int comzen) {
		this.comzen = comzen;
	}
	
	
}
