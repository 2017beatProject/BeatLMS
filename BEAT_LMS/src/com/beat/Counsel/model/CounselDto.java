package com.beat.Counsel.model;

public class CounselDto {

	private int mnum;
	private String mname;
	private String counselreq;
	
	public int getMnum() {
		return mnum;
	}
	public void setMnum(int mnum) {
		this.mnum = mnum;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public String getCounselreq() {
		return counselreq;
	}
	public void setCounselreq(String counselreq) {
		this.counselreq = counselreq;
	}
	
	
	
}
